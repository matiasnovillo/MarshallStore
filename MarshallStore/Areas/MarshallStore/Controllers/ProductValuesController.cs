        [HttpPost("~/api/MarshallStore/Product/1/InsertOrUpdateAsync")]
        public async Task<IActionResult> InsertOrUpdateAsync()
        {
            try
            {
                //Get UserId from Session
                int UserId = HttpContext.Session.GetInt32("UserId") ?? 0;

                if(UserId == 0)
                {
                    return StatusCode(401, "User not found in session");
                }
                
                #region Pass data from client to server
                //ProductId
                int ProductId = Convert.ToInt32(HttpContext.Request.Form["marshallstore-product-productid-input"]);
                
                int ProductCategoryId = 0; 
                if (Convert.ToInt32(HttpContext.Request.Form["marshallstore-product-productcategoryid-input"]) != 0)
                {
                    ProductCategoryId = Convert.ToInt32(HttpContext.Request.Form["marshallstore-product-productcategoryid-input"]);
                }
                else
                { return StatusCode(400, "It's not allowed to save zero values in ProductCategoryId"); }
                string Producer = HttpContext.Request.Form["marshallstore-product-producer-input"];
                string Model = HttpContext.Request.Form["marshallstore-product-model-input"];
                decimal Price = Convert.ToDecimal(HttpContext.Request.Form["marshallstore-product-price-input"].ToString().Replace(".",","));
                string Description = HttpContext.Request.Form["marshallstore-product-description-input"];
                string Image1 = HttpContext.Request.Form["marshallstore-product-image1-input"];;
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    Image1 = $@"/Uploads/MarshallStore/Product/{HttpContext.Request.Form.Files[0].FileName}";
                }
                string Image2 = HttpContext.Request.Form["marshallstore-product-image2-input"];;
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    Image2 = $@"/Uploads/MarshallStore/Product/{HttpContext.Request.Form.Files[1].FileName}";
                }
                string Image3 = HttpContext.Request.Form["marshallstore-product-image3-input"];;
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    Image3 = $@"/Uploads/MarshallStore/Product/{HttpContext.Request.Form.Files[2].FileName}";
                }
                
                #endregion

                int NewEnteredId = 0;
                int RowsAffected = 0;

                if (ProductId == 0)
                {
                    //Insert
                    ProductModel ProductModel = new ProductModel()
                    {
                        Active = true,
                        UserCreationId = UserId,
                        UserLastModificationId = UserId,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        ProductCategoryId = ProductCategoryId,
                        Producer = Producer,
                        Model = Model,
                        Price = Price,
                        Description = Description,
                        Image1 = Image1,
                        Image2 = Image2,
                        Image3 = Image3,
                        
                    };
                    
                    NewEnteredId = _IProduct.Insert(ProductModel);
                }
                else
                {
                    //Update
                    ProductModel ProductModel = new ProductModel(ProductId);
                    
                    ProductModel.UserLastModificationId = UserId;
                    ProductModel.DateTimeLastModification = DateTime.Now;
                    ProductModel.ProductCategoryId = ProductCategoryId;
                    ProductModel.Producer = Producer;
                    ProductModel.Model = Model;
                    ProductModel.Price = Price;
                    ProductModel.Description = Description;
                    ProductModel.Image1 = Image1;
                    ProductModel.Image2 = Image2;
                    ProductModel.Image3 = Image3;
                                       

                    RowsAffected = _IProduct.UpdateByProductId(ProductModel);
                }
                

                //Look for sent files
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    int i = 0; //Used to iterate in HttpContext.Request.Form.Files
                    foreach (var File in Request.Form.Files)
                    {
                        if (File.Length > 0)
                        {
                            var FileName = HttpContext.Request.Form.Files[i].FileName;
                            var FilePath = $@"{_WebHostEnvironment.WebRootPath}/Uploads/MarshallStore/Product/";

                            using (var FileStream = new FileStream($@"{FilePath}{FileName}", FileMode.Create))
                            {
                                
                                await File.CopyToAsync(FileStream); // Read file to stream
                                byte[] array = new byte[FileStream.Length]; // Stream to byte array
                                FileStream.Seek(0, SeekOrigin.Begin);
                                FileStream.Read(array, 0, array.Length);
                            }

                            i += 1;
                        }
                    }
                }

                if (ProductId == 0)
                {
                    return StatusCode(200, NewEnteredId); 
                }
                else
                {
                    return StatusCode(200, RowsAffected);
                }
            }
            catch (Exception ex) 
            { 
                DateTime Now = DateTime.Now;
                FailureModel FailureModel = new FailureModel()
                {
                    HTTPCode = 500,
                    Message = ex.Message,
                    EmergencyLevel = 1,
                    StackTrace = ex.StackTrace ?? "",
                    Source = ex.Source ?? "",
                    Comment = "",
                    Active = true,
                    UserCreationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    UserLastModificationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    DateTimeCreation = Now,
                    DateTimeLastModification = Now
                };
                FailureModel.Insert();
                return StatusCode(500, ex.Message);
            }
        }

