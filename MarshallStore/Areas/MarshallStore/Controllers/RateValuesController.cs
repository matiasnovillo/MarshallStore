        [HttpPost("~/api/MarshallStore/Rate/1/InsertOrUpdateAsync")]
        public async Task<IActionResult> InsertOrUpdateAsync()
        {
            try
            {
                //Get UserId from Session
                int UserId = HttpContext.Session.GetInt32("UserId") ?? 0;

                if(UserId == 0)
                {
                    return StatusCode(401, "User not found in session");
                }
                
                #region Pass data from client to server
                //RateId
                int RateId = Convert.ToInt32(HttpContext.Request.Form["marshallstore-rate-rateid-input"]);
                
                int ProductId = 0; 
                if (Convert.ToInt32(HttpContext.Request.Form["marshallstore-rate-productid-input"]) != 0)
                {
                    ProductId = Convert.ToInt32(HttpContext.Request.Form["marshallstore-rate-productid-input"]);
                }
                else
                { return StatusCode(400, "It's not allowed to save zero values in ProductId"); }
                int Score = Convert.ToInt32(HttpContext.Request.Form["marshallstore-rate-score-input"]);
                
                #endregion

                int NewEnteredId = 0;
                int RowsAffected = 0;

                if (RateId == 0)
                {
                    //Insert
                    RateModel RateModel = new RateModel()
                    {
                        Active = true,
                        UserCreationId = UserId,
                        UserLastModificationId = UserId,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        ProductId = ProductId,
                        Score = Score,
                        
                    };
                    
                    NewEnteredId = _IRate.Insert(RateModel);
                }
                else
                {
                    //Update
                    RateModel RateModel = new RateModel(RateId);
                    
                    RateModel.UserLastModificationId = UserId;
                    RateModel.DateTimeLastModification = DateTime.Now;
                    RateModel.ProductId = ProductId;
                    RateModel.Score = Score;
                                       

                    RowsAffected = _IRate.UpdateByRateId(RateModel);
                }
                

                //Look for sent files
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    int i = 0; //Used to iterate in HttpContext.Request.Form.Files
                    foreach (var File in Request.Form.Files)
                    {
                        if (File.Length > 0)
                        {
                            var FileName = HttpContext.Request.Form.Files[i].FileName;
                            var FilePath = $@"{_WebHostEnvironment.WebRootPath}/Uploads/MarshallStore/Rate/";

                            using (var FileStream = new FileStream($@"{FilePath}{FileName}", FileMode.Create))
                            {
                                
                                await File.CopyToAsync(FileStream); // Read file to stream
                                byte[] array = new byte[FileStream.Length]; // Stream to byte array
                                FileStream.Seek(0, SeekOrigin.Begin);
                                FileStream.Read(array, 0, array.Length);
                            }

                            i += 1;
                        }
                    }
                }

                if (RateId == 0)
                {
                    return StatusCode(200, NewEnteredId); 
                }
                else
                {
                    return StatusCode(200, RowsAffected);
                }
            }
            catch (Exception ex) 
            { 
                DateTime Now = DateTime.Now;
                FailureModel FailureModel = new FailureModel()
                {
                    HTTPCode = 500,
                    Message = ex.Message,
                    EmergencyLevel = 1,
                    StackTrace = ex.StackTrace ?? "",
                    Source = ex.Source ?? "",
                    Comment = "",
                    Active = true,
                    UserCreationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    UserLastModificationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    DateTimeCreation = Now,
                    DateTimeLastModification = Now
                };
                FailureModel.Insert();
                return StatusCode(500, ex.Message);
            }
        }

