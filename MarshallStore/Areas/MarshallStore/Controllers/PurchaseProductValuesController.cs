        [HttpPost("~/api/MarshallStore/PurchaseProduct/1/InsertOrUpdateAsync")]
        public async Task<IActionResult> InsertOrUpdateAsync()
        {
            try
            {
                //Get UserId from Session
                int UserId = HttpContext.Session.GetInt32("UserId") ?? 0;

                if(UserId == 0)
                {
                    return StatusCode(401, "User not found in session");
                }
                
                #region Pass data from client to server
                //PurchaseProductId
                int PurchaseProductId = Convert.ToInt32(HttpContext.Request.Form["marshallstore-purchaseproduct-purchaseproductid-input"]);
                
                int PurchaseId = 0; 
                if (Convert.ToInt32(HttpContext.Request.Form["marshallstore-purchaseproduct-purchaseid-input"]) != 0)
                {
                    PurchaseId = Convert.ToInt32(HttpContext.Request.Form["marshallstore-purchaseproduct-purchaseid-input"]);
                }
                else
                { return StatusCode(400, "It's not allowed to save zero values in PurchaseId"); }
                int ProductId = 0; 
                if (Convert.ToInt32(HttpContext.Request.Form["marshallstore-purchaseproduct-productid-input"]) != 0)
                {
                    ProductId = Convert.ToInt32(HttpContext.Request.Form["marshallstore-purchaseproduct-productid-input"]);
                }
                else
                { return StatusCode(400, "It's not allowed to save zero values in ProductId"); }
                
                #endregion

                int NewEnteredId = 0;
                int RowsAffected = 0;

                if (PurchaseProductId == 0)
                {
                    //Insert
                    PurchaseProductModel PurchaseProductModel = new PurchaseProductModel()
                    {
                        Active = true,
                        UserCreationId = UserId,
                        UserLastModificationId = UserId,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        PurchaseId = PurchaseId,
                        ProductId = ProductId,
                        
                    };
                    
                    NewEnteredId = _IPurchaseProduct.Insert(PurchaseProductModel);
                }
                else
                {
                    //Update
                    PurchaseProductModel PurchaseProductModel = new PurchaseProductModel(PurchaseProductId);
                    
                    PurchaseProductModel.UserLastModificationId = UserId;
                    PurchaseProductModel.DateTimeLastModification = DateTime.Now;
                    PurchaseProductModel.PurchaseId = PurchaseId;
                    PurchaseProductModel.ProductId = ProductId;
                                       

                    RowsAffected = _IPurchaseProduct.UpdateByPurchaseProductId(PurchaseProductModel);
                }
                

                //Look for sent files
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    int i = 0; //Used to iterate in HttpContext.Request.Form.Files
                    foreach (var File in Request.Form.Files)
                    {
                        if (File.Length > 0)
                        {
                            var FileName = HttpContext.Request.Form.Files[i].FileName;
                            var FilePath = $@"{_WebHostEnvironment.WebRootPath}/Uploads/MarshallStore/PurchaseProduct/";

                            using (var FileStream = new FileStream($@"{FilePath}{FileName}", FileMode.Create))
                            {
                                
                                await File.CopyToAsync(FileStream); // Read file to stream
                                byte[] array = new byte[FileStream.Length]; // Stream to byte array
                                FileStream.Seek(0, SeekOrigin.Begin);
                                FileStream.Read(array, 0, array.Length);
                            }

                            i += 1;
                        }
                    }
                }

                if (PurchaseProductId == 0)
                {
                    return StatusCode(200, NewEnteredId); 
                }
                else
                {
                    return StatusCode(200, RowsAffected);
                }
            }
            catch (Exception ex) 
            { 
                DateTime Now = DateTime.Now;
                FailureModel FailureModel = new FailureModel()
                {
                    HTTPCode = 500,
                    Message = ex.Message,
                    EmergencyLevel = 1,
                    StackTrace = ex.StackTrace ?? "",
                    Source = ex.Source ?? "",
                    Comment = "",
                    Active = true,
                    UserCreationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    UserLastModificationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    DateTimeCreation = Now,
                    DateTimeLastModification = Now
                };
                FailureModel.Insert();
                return StatusCode(500, ex.Message);
            }
        }

